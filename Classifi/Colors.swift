import UIKit

class Colors {
    class func classifiPurple() -> UIColor! {
        return UIColor(red:131.0/255.0, green:79.0/255.0, blue:229.0/355.0, alpha:1.0)
    }
    
    class func invalidRed() -> UIColor! {
        return UIColor(red:220.0/255.0, green:0.0/255.0, blue:0.0/355.0, alpha:1.0)
    }
    
    class func asyncBoxBlack() -> UIColor! {
        return UIColor(white: 0.11, alpha: 0.73)
    }
}
